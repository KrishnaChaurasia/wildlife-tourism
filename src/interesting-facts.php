					<br><br><h1>10 Fascinating Facts About Indian Wildlife</h1>
					<p class='text-justify'>
						Wildlife is one of the major fundaments of India&#8217;s Hindu religion, since a great many of their deities revisit the Earth on four legs. But animals are integral to the culture of India in more than just religious ways. Below are 10 intriguing facts about the animals of India, from the deadly to the drunken to the disgusting.
					</p>
					<h2>#10 Puppy Pregnancy Syndrome</h2>
					<p>
						<img src="images/Interesting-Facts/image1.jpg" alt="hanging" width="100%" />
					</p>
					<p class='text-justify'>
						Dog bites are a problem around the globe, but in the Western world, most canines are inoculated against rabies, and it&#8217;s merely a painful nuisance.  In India, the repercussions can be far worse.  The country boasts more stray dogs than any other on earth&#8212;tens of millions attacking humans at will.  It&#8217;s estimated that some 20,000 people die of rabies in India every year.
					</p>
					<p class='text-justify'>
						In more rural areas, a bizarre phenomenon has cropped up where those bitten by dogs believe they&#8217;ve been impregnated by them and puppies are growing in their bellies.  Puppy pregnancy syndrome is a disturbing form of mass hysteria that is most prevalent in those with little education.  Sufferers claim they can feel the puppies moving around inside them and often revert to bizarre behaviors like barking.  Instead of seeking medical treatment, these people often seek out the help of a witch doctor&#8212;who gives oral treatments designed to dissolve the puppies, which are then &#8220;passed normally through the digestive tract.&#8221;
					</p>


					<h2>#9 Monkey Trouble</h2>
					<p>
						<img src="images/Interesting-Facts/image2.jpg" alt="angry-monkey" width="100%" />
					</p>
					<p class='text-justify'>
						Like many other creatures, India considers its monkeys sacred.  As such, they are far too indulgent to the thousands and thousands of rhesus monkeys that crowd city streets.  The creatures are incredibly dangerous: prone to destroying homes and biting passersby.  Hordes of monkeys will literally mug people&#8212;taking their food and ripping off their clothing.  Cities like New Delhi, where the plague is particularly terrible, have instituted trapping policies. But in many cases, the animals are far too intelligent to fall for the traps.
					</p>
					<p class='text-justify'>
						In 2007, Sawinder Singh Bajwa toppled from his balcony and died trying to fend off a gang of monkeys that attacked him. Though people are attacked daily, they continue to feed the animals.  The problem has no end in sight; Hindu law dictates that the monkeys cannot be euthanized, and residents complain that those taken to sanctuaries merely scale the walls and return to the city.
					</p>

					<h2>#8 Pig Toilets</h2>
					<p>
						<img src="images/Interesting-Facts/image3.jpg" alt="pig" width="100%" />
					</p>
					<p class='text-justify'>
						Pigs are undoubtedly the most brilliant of barnyard animals, at least on par with dogs.  Despite their cunning, swine are often overcome with an extreme hunger, and they will devour anything even vaguely edible&#8212;from garbage to the flesh of fellow hogs and even human feces.
					</p>
					<p class='text-justify'>
						The Goa pig toilet is a rural outhouse over a pit that runs into a pig enclosure.  The pigs gratefully accept whatever drops into their trough.  What it lacks in hygiene, it makes up for in efficiency.  Modern plumbing is slowly making the pig toilet a thing of the past, but for now, pig outhouses are still a thing in India.
					</p>
					<h2>#7 Indian Vulture Crisis</h2>
					<p>
						<img src="images/Interesting-Facts/image4.jpg" alt="vulture" width="100%" />
					</p>
					<p class='text-justify'>
						Evolution has designated vultures to be the ultimate scavengers.  Enormous wingspans allow them to circle in the air for hours.  Their beaks, while rather horrifying, are weak by bird standards, made to scoop and devour flesh.    However unappealing they may seem, vultures serve an important role in the ecological cycle: processing carcasses.
					</p>
					<p class='text-justify'>
						Only 20 years ago, India was inundated with vultures&#8212;flocks so enormous they darkened the skies.  But by 1999, their numbers had dropped dramatically, due to a mysterious kidney ailment.  By 2008, 99.9 percent of India’s vultures were gone.  It was finally discovered that they had been killed by a drug called diclofenac (a pain reliever along the lines of aspirin or ibuprofen).  Indians revere their cows, and when a cow showed signs of pain, they treated it with diclofenac.  After the animal died, the vultures would eat the corpse.  And though they boast perhaps the world’s most efficient digestive system, vultures cannot handle the drug.
					</p>
					<p class='text-justify'>
						India banned the use of diclofenac for veterinary use in 2006, but it&#8217;s still widely used.  The near extinction of vultures has caused a great spike in disease in the country, as rats and feral dogs moved in to take their place&#8212;spreading pathogens that would have otherwise been destroyed in the gut of the carrion bird.
					</p>
					<h2>#6 Tigers</h2>
					<p>
						<img src="images/Interesting-Facts/image5.jpg" alt="tiger" width="100%" />
					</p>
					<p class='text-justify'>
						Tigers once roamed India in the tens of thousands.  Always a danger, villagers knew not to stray too far from the fires at night lest they become an entrée.  But man may be responsible for most of the &#8220;maneater&#8221; tigers.  Killing a tiger is serious business; unless shot in a vital organ, it is unlikely to die.  Instead, the wounded animal would flee, but crippled by its injuries, it would be forced to hunt meeker prey.  And to a tiger in India, that usually means unsuspecting humans.
					</p>
					<p class='text-justify'>
						The Champawat tigress ranks as the deadliest maneater in recorded history with over 400 kills to her name.  In 1907, legendary hunter Jim Corbett ended her reign of terror.  Tragically, the tigress was shown to have been shot in the mouth some time earlier, which shattered her fangs and rendered her incapable of taking regular prey.
					</p>
					<p class='text-justify'>
						Today, there are few tigers left in the wild&#8212;as few as 3,200 according to the World Wildlife Fund.  They still claim dozens of human lives yearly, especially in the Sundarbans, a mangrove forest where some 500 Bengal tigers roam.  It is hypothesized that the brackish water causes the tigers to become irritable and unnaturally aggressive.  Fishermen who enter the area sometimes wear masks with faces on the back of their heads because tigers have a strong preference for ambushing their prey from behind.
					</p>
					<h2>#5 Drunken Elephants</h2>
					<p>
						<img src="images/Interesting-Facts/image6.jpg" alt="elephant" width="100%" />
					</p>
					<p class='text-justify'>
						We can’t help being captivated by elephants; they are so much like us&#8212;empathic and intelligent.  The most talented specimens have been known to paint self portraits and even imitate human speech.  And much like us, sometimes they need a hard drink.  In 2012, a herd of about 50 elephants were drawn out of the jungle when they smelled <em>mahua</em>, a strong moonshine-like liquor.  Downing some 500 liters of the alcohol, they proceeded to destroy dozens of houses in the village of Dumurkota.  After three hours, they were finally herded out of town, but the damage was done.
					</p>
					<p class='text-justify'>
						This was hardly the first case of elephants going on a drunken rampage. The problem has become so widespread, that in some areas fences strung up with chili peppers have been erected. The peppers, sometimes called “ghost chilies” are among the hottest in the world, and their pungency is enough to turn back even the most determined pachyderm.
					</p>
					<h2>#4 The Goonch</h2>
					<p>
						<img src="images/Interesting-Facts/image7.jpg" alt="catfish" width="100%" />
					</p>
					<p class='text-justify'>
						Catfish might be ugly, but they hardly evoke thoughts of danger.  However, in the Kali River of India, the goonch&#8212;giant devil catfish&#8212;is believed to be responsible for several human deaths.  Hindu funeral rituals dictate bodies be reduced to ash and then interred in rivers, but oftentimes charred remains are eaten by the goonch.  With an appetite for human flesh, this catfish, which can grow well over 150 lbs, has been implicated in the disappearance of swimmers. While the goonch lacks the fearsome nature of a shark, a specimen as large as a man would easily be able to seize someone by the ankle, drag them underwater, and drown them.
					</p>
					<h2>#3 Lions</h2>
					<p>
						<img src="images/Interesting-Facts/image8.jpg" alt="lion" width="100%" />
					</p>
					<p class='text-justify'>
						India belongs to the tiger, but in the Gir Forest National Park and Wildlife Sanctuary, another massive cat can be found: the rare Asiatic lion.  Lions once roamed a vast swath of real estate from Africa through the Middle East, India, Greece, and Hungary.  Today, their range has greatly diminished.  The Gir lions number only about 400 and are separated from their African cousins by tens of thousands of years of evolution.  They are slightly smaller than African lions and have a diminished mane.  Unfortunately, there are so few of these creatures left that they are very inbred.  A single forest fire or outbreak of disease could wipe out the entire species.  A handful of the lions are being relocated to a neighboring state in an effort to ensure the survival of the species.
					</p>
					<h2>#2 Temple Of The Rat</h2>
					<p>
						<img src="images/Interesting-Facts/image9.jpg" alt="rats" width="100%" />
					</p>
					<p class='text-justify'>
						A rat scuttling across the kitchen floor in the night is enough to make a lot of people jump on the furniture.  But in the Karni Mata Temple, the rat is revered as the living manifestation of Hindu deities.  As the story goes, Karni Mata was a Hindu sage&#8212;the incarnation of a goddess named Durga.  When one of Mata’s stepsons drowned, she beseeched Yama, the god of death, to bring him back.  In a cruel turn of events, Yama resurrected all of Karni Mata’s sons as rats.  
					</p>
					<p class='text-justify'>
						Built by Maharaja Sir Ganga Singh, the ornate marble temple is home to some 15,000 black rats, which are fed milk and sweets by devotees.  There are a small handful of white rats amidst the horde, which are believed to be the reincarnations of Karni Mata and her sons, and spotting them is considered to be especially fortuitous.  The temple is open to the public, but beware&#8212;the rats are completely unafraid of people.  And you aren’t allowed to wear shoes.
					</p>
					<h2>#1 The Mongoose And The Cobra</h2>
					<p>
						<img src="images/Interesting-Facts/image10.jpg" alt="cobra" width="100%" />
					</p>
					<p class='text-justify'>
						The king cobra is perhaps the most feared snake in the world. And with good reason: a full grown specimen can rise high enough to look the average human in the eye.  Its hiss is terrifyingly guttural, often compared to the growl of an angry dog.  A full payload of king cobra venom is enough to kill 20 people.  But even this nightmare of nature is not without enemies.
					</p>
					<p class='text-justify'>
						Enter the mongoose: a cute little guy roughly the size of a ferret. But the mongoose is a dexterous predator.  The adorable little mongoose is quick, agile, and capable of killing cobras and other snakes with merciless efficiency.  Should they be bitten, they have certain immunities to venom, but oftentimes they are able to take out the snake without being struck.  A common method of dispatch is to dance from side to side, dart out of the cobra’s way as it strikes, and seize it behind the skull.
					</p>

